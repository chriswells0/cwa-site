					<div class="content-body">
						<p>To update this page, <a href="/account/login">log in</a> as an admin or developer and edit <a href="/admin/code/views~Site~about.html.php">about.html.php</a>.</p>
						<p><a href="https://github.com/chriswells0/cwa-site" title="View the CWA Site project" rel="external">Core Web Application Site</a> was built to provide a fully-functional example of how to use the <a href="https://github.com/chriswells0/cwa-lib" title="View the CWA Libraries project" rel="external">Core Web Application Libraries</a> to create a basic website. It gives developers a baseline to launch new sites quickly by addressing many of the details that should be included in every site. It follows industry best practices to make it simpler for developers to do the same.</p>
						<p>Some features included in <a href="https://github.com/chriswells0/cwa-site" title="View the CWA Site project" rel="external">Core Web Application Site</a>:</p>
						<ul>
							<li>A "starter" website with a home page, about page, and contact form.</li>
							<li>Responsive design that works well at multiple screen resolutions on devices of all sizes.</li>
							<li>Search engine (and human!) friendly URLs with a consistent format: /controller/method/parameter</li>
							<li>Includes structured data to improve indexing by search engines.</li>
							<li>Error pages that can be easily customized for all errors or by HTTP status code.</li>
							<li>Site Admin section with multiple tools to facilitate common tasks:
								<ul>
									<li>Code Editor</li>
									<li>DB Administrator</li>
									<li>Log Viewer</li>
									<li>QA Assistant</li>
								</ul>
							</li>
							<li>Lightweight and flexible base classes make it easy to master and extend the code.</li>
							<li>Uses the MVC design pattern and other web application best practices.</li>
							<li>Many built-in protections against common web application vulnerabilities/exploits:
								<ul>
									<li>Primarily uses prepared statements to deter SQL injection attacks.</li>
									<li>Clickjacking defenses encompass multiple headers as well as JavaScript.</li>
									<li>Automatic sanitization of simple variables passed to views and easy sanitization of other content to defend against cross-site scripting (XSS).</li>
									<li>Cross-site request forgery (CSRF) prevention using the synchronizer token pattern for all POST requests.</li>
									<li>Role-based method access is straightforward to configure and a cinch to validate with the QA Assistant.</li>
									<li>User passwords are stored strongly hashed and salted.</li>
									<li>Full session teardown and recreation upon login to inhibit session fixation.</li>
									<li>Sessions are pinned to the user's IP and user agent string to thwart hijacking.</li>
								</ul>
							</li>
							<li>Plus even more features provided by the <a href="https://github.com/chriswells0/cwa-lib" title="View the CWA Libraries project" rel="external">Core Web Application Libraries</a>!</li>
						</ul>
